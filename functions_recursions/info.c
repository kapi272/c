/*
Parameters are variables defined in the function
declaration/definition that act as placeholders for
the values the function will use.

Arguments are the actual values you pass to the 
function when you call it.

=========================================================
Example from your code:

int sum(int a, int b) // a and b are parameters
...
sum(x, y);            // x and y are arguments
=========================================================

Parameters: Used in function definition (int a, int b)
Arguments: Used in function call (sum(x, y))

*/

